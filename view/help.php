    Links &amp; Groups
    <hr>
    <div style="float:left; padding:10px;"><img src="/images/help/link_menu.png" /></div>
    <div style="padding:10px;">Start by clicking on the <b>Links</b> menu and selecting <b>New Group</b>.</div>
    <div class="clear"></div>
    </div>
    <div>
    <div style="float:left; padding:10px;"><img src="/images/help/group_dialog.png" /></div>
    <div style="padding:10px;">In the dialog that appears, enter the name of a group you want to create. For example, <b>Shopping</b>. Click the <b>OK</b> button to add a new group.</div>
    <div class="clear"></div>
    </div>
    <div>
    <div style="float:left; padding:10px;"><img src="/images/help/shop_group.png" /></div>
    <div style="padding:10px;">A new group will appear. You can move the group anywhere you want by clicking and dragging on the group name. When you move your mouse over the group title, a toolbar will appear with the <b>More</b> <img src="/images/more_ico.png"> and <b>Launch</b> <img src="/images/launch_ico.png"> icons.</div>
    <div class="clear"></div>
    <div style="float:left; padding:10px;"><img src="/images/help/shop_group_tb.png" /></div>
    <div style="padding:10px;">Click on the <b>More</b> <img src="/images/more_ico.png"> icon to reveal the rest of the toolbar. You can add links to this group by clicking on the <b>Add Link</b> <img src="/images/link_ico.png"> icon. </div>
    <div class="clear"></div>
    <div style="float:left; padding:10px;"><img src="/images/help/link_dialog.png" /></div>
    <div style="padding:10px;">Clicking on the <b>Add Link</b> <img src="/images/link_ico.png"> icon will bring up the Link Dialog box where you can create a new link. Type in the name and URL of the website you want to add, and click the <b>OK</b> button to add the link.</div>
    <div class="clear"></div>
    <div style="float:left; padding:10px;"><img src="/images/help/group_link.png" /></div>
    <div style="padding:10px;">The link will be added to the group. You can repeat these steps to create more groups and add additional links. Additionally, links can be moved between groups or reordered by simply clicking and dragging on the link. If you want to simultaneously open all links in a group, just click the <b>Launch</b> <img src="/images/launch_ico.png"> icon from the group toolbar.</div>
    <div class="clear"></div>
    </div>
