    Objects
    <hr>
    Objects are handy for adding extra stuff to your page. For example, you can use an HTML object to add Google Search to your page.<br>
    <br>
    <div>
    <div style="float:left; padding:10px;"><img src="/images/help/html_menu.png" /></div>
    <div style="padding:10px;">Start by selecting <b>New HTML</b> from the <b>Links</b> menu.</div>
    <div class="clear"></div>
    </div>
    <div>
    <div style="float:left; padding:10px;"><img src="/images/help/html_dialog.png" /></div>
    <div style="padding:10px;">In the dialog that appears, select one of the <b>Premade</b> objects by clicking on the link down at the bottom. When clicked, the name and code sections will be automatically filled out. Alternatively, you can create your own objects by typing in a name and the HTML code you want it to use. Click the <b>OK</b> button once the name and code sections are filled out to create your object.</div>
    <div class="clear"></div>
    </div>
    <div>
    <div style="float:left; padding:10px;"><img src="/images/help/goog_obj.png" /></div>
    <div style="padding:10px;">You can now resize the object by clicking and dragging on its borders. You can also move the object by clicking and dragging it by its titlebar.</div>
