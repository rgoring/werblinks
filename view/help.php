    Workspaces
    <hr>
    <ul>
    	<li>New Workspace (menu) - Creates a new workspace. Workspaces are good for separating links. For example, you can put personal links on one workspace and put work related links on a separate workspace. This allows you to use the same account for both home and work.</li>
        <li>Workspace List - Below the  <b>New Workspace</b> menu item is a list of all the workspaces you currently have. Select a workspace to switch to it.</li>
    </ul>
    <br><br>
    Links
	<hr>
    <ul>
    	<li>Link Bucket (menu) - A non-organized group of links at the top of each workspace. Links from the Link Bucket can be dragged to a group.</li>
        <li>New Link (menu) - Creates a new link and adds it to the Link Bucket</li>
        <li>Moving links - links can be dragged from group to group or dragged within the same groups to be reordered</li>
    </ul>
<br><br>
    Groups
	<hr>
    <ul>
    	<li>New Group (menu) - Creates a new empty group on the current workspace.</li>
        <li>Launch <img src="/images/launch_ico.png"> - Opens each link in the group to a new tab/window</li>
        <li>Link <img src="/images/link_ico.png"> - Create a new link and add it to the Group instead of the Link Bucket</li>
        <li>Edit <img src="/images/edit_ico.png"> - Allows you to edit the group details</li>
        <li>Toggle Sticky <img src="/images/sticky_ico.png"> - If "sticky", the Group will appear on all workspaces. If not, the group will only be available on the current workspace.</li>
        <li>Delete <img src="/images/del_ico.png"> - Deletes the group and all links in it</li>
        <li>Moving Groups - Groups can be dragged by their title bar and positioned wherever you want</li>
        <li>Resizing Groups - Groups can only have their width adjusted. Height will be adjusted automatically based on the number of links in the group</li>
    </ul>
<br><br>
    HTML Objects
	<hr>
    <ul>
    	<li>New HTML (menu) - Creates a new HTML object on the current workspace</li>
        <li>Edit <img src="/images/edit_ico.png"> - Allows you to edit the HTML object name and code</li>
        <li>Toggle Sticky <img src="/images/sticky_ico.png"> - If "sticky", the HTML Object will appear on all workspaces. If not, the object will only be available on the current workspace.</li>
        <li>Delete <img src="/images/del_ico.png"> - Deletes the HTML object</li>
        <li>Moving Objects - Objects can be dragged by their title bar and positioned wherever you want</li>
        <li>Resizing Objects - Objects can be resized to fit the contents</li>
    </ul>
